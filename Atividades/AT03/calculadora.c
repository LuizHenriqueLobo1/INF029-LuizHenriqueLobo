#include <stdio.h>

int soma(int val1, int val2) {

	int resultado = 0;

	resultado = val1 + val2;

	return resultado;
}

int subtracao(int val1, int val2) {

	int resultado = 0;

	resultado = val1 - val2;

	return resultado;
}

int multiplicacao(int val1, int val2) {

	int resultado = 0;

	resultado = val1 * val2;

	return resultado;
}

int divisao(int val1, int val2) {

	int resultado = 0;

	resultado = val1 / val2;

	return resultado;
}